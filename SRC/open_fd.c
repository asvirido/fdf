#include "../head.h"

int		open_fd(char *argv)
{
	int		fd;

	if ((fd = open(argv, O_RDONLY)) == -1)
	{
		ft_putendl("ERROR: OPEN FD");
		exit(0);
	}
	return (fd);
}
